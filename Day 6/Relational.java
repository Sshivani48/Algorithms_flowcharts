public class Relational
{
	public static void main(String[] args) {
		System.out.println("Relational Operators");
		int a=10;
		int b=20;
		int c=30;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(c>b);
		System.out.println(a<c);
		System.out.println(a==b);
		System.out.println(a!=b);
	}
}
